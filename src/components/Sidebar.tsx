interface User {
  fullName: string
  email: string
  role: string
}

export default function Sidebar() {
  const pathname = usePathname()
  const router = useRouter()
  const [user, setUser] = useState<User | null>(null)
  const { inviteCount, setInviteCount } = useInvites()

  useEffect(() => {
    const fetchData = async () => {
      try {
        // Fetch user and invites in parallel
        const [userResponse, invitesResponse] = await Promise.all([
          fetch('/api/auth/me'),
          fetch('/api/invites'),
        ])

        if (userResponse.ok) {
          const data = await userResponse.json()
          setUser(data.user)
        }

        if (invitesResponse.ok) {
          const data = await invitesResponse.json()
          setInviteCount(data.invites?.length || 0)
        }
      } catch {
        // User not authenticated or error
      }
    }
    fetchData()
  }, [setInviteCount])

  const handleLogout = async () => {
    try {
      await fetch('/api/auth/logout', { method: 'POST' })
      router.push('/login')
    } catch {
      // Handle error
    }
  }

  const getInitials = (name: string) => {
    return name
      .split(' ')
      .map((n) => n[0])
      .join('')
      .toUpperCase()
      .slice(0, 2)
  }

  return (
    <aside className="w-64 bg-sidebar min-h-screen fixed left-0 top-0 flex flex-col">
      {/* Logo */}
      <div className="p-6 border-b border-sidebar-foreground/10">
        <div className="flex items-center gap-3">
          <div className="w-10 h-10 bg-primary rounded-[0.625rem] flex items-center justify-center">
            <svg className="w-5 h-5 text-primary-foreground" fill="none" viewBox="0 0 24 24" stroke="currentColor">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M12 14l9-5-9-5-9 5 9 5z" />
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M12 14l9-5-9-5-9 5 9 5z" />
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M12 14l6.16-3.422a12.083 12.083 0 01.665 6.479A11.952 11.952 0 0012 20.055a11.952 11.952 0 00-6.824-2.998 12.078 12.078 0 01.665-6.479L12 14z" />
            </svg>
          </div>
          <div>
            <span className="text-sidebar-foreground font-bold text-lg">UPT Projects</span>
            <p className="text-sidebar-muted text-xs">Project Management</p>
          </div>
        </div>
      </div>

      {/* Search Bar */}
      <div className="px-4 py-3">
        <SearchBar />
      </div>

      {/* Navigation */}
      <nav className="flex-1 p-4 pt-0 overflow-y-auto">
        {/* Show appropriate navigation based on user role */}
        {(() => {
          const navItems = user?.role === 'professor' ? professorNavItems : studentNavItems
          return (
            <ul className="space-y-1">
              {navItems.map((item) => {
                const isActive = pathname === item.href ||
                  (item.href.includes('?') && pathname === item.href.split('?')[0])
                const showBadge = item.hasBadge && inviteCount > 0
                return (
                  <li key={item.name}>
                    <Link
                      href={item.href}
                      className={`
                        flex items-center gap-3 px-4 py-3 rounded-[0.625rem] transition-all duration-200
                        ${isActive
                          ? 'bg-primary text-primary-foreground'
                          : 'text-sidebar-muted hover:bg-sidebar-foreground/5 hover:text-sidebar-foreground'
                        }
                      `}
                    >
                      {item.icon}
                      <span className="font-medium flex-1">{item.name}</span>
                      {showBadge && (
                        <span className={`
                          px-2 py-0.5 text-xs font-semibold rounded-full
                          ${isActive ? 'bg-primary-foreground text-primary' : 'bg-primary text-primary-foreground'}
                        `}>
                          {inviteCount}
                        </span>
                      )}
                    </Link>
                  </li>
                )
              })}
            </ul>
          )
        })()}
      </nav>

      {/* User section */}
      <div className="p-4 border-t border-sidebar-foreground/10">
        <div className="flex items-center gap-3 px-4 py-3">
          <div className="w-10 h-10 bg-primary/20 rounded-full flex items-center justify-center">
            <span className="text-primary font-semibold text-sm">
              {user ? getInitials(user.fullName) : '...'}
            </span>
          </div>
          <div className="flex-1 min-w-0">
            <p className="text-sidebar-foreground text-sm font-medium truncate">
              {user?.fullName || 'Loading...'}
            </p>
            <p className="text-sidebar-muted text-xs truncate">
              {user?.email || ''}
            </p>
          </div>
          <NotificationBell />
          <button
            onClick={handleLogout}
            className="text-sidebar-muted hover:text-destructive transition-colors p-2 rounded-[0.625rem] hover:bg-destructive/10"
            title="Logout"
          >
            <svg className="w-5 h-5" fill="none" viewBox="0 0 24 24" stroke="currentColor">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M17 16l4-4m0 0l-4-4m4 4H7m6 4v1a3 3 0 01-3 3H6a3 3 0 01-3-3V7a3 3 0 013-3h4a3 3 0 013 3v1" />
            </svg>
          </button>
        </div>
      </div>
    </aside>
  )
}
